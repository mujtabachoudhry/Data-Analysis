# Rules for naming a variable:
# 1- The variable name should only contain letters, numbers or underscores
# 2- Do not start with numbers
# 3- Spaces are not allowed
# 4- Do not use keywords as variable names (if, for, while, etc.)
# 5- Variable names are case sensitive 
# 6- Variable names should be descriptive 
# 7- Variable names should be short
# 8- Variable names should be unique
# 9- Variable names should be meaningful

